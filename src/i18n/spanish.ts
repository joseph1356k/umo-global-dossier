export function normalizeSpanishText(text: string) {
  return replacements.reduce((result, [pattern, value]) => result.replace(pattern, value), text);
}

function normalizeNode<T>(value: T, key?: string): T {
  if (typeof value === "string") {
    if (key && excludedStringKeys.has(key)) return value;
    return normalizeSpanishText(value) as T;
  }

  if (Array.isArray(value)) {
    return value.map((item) => normalizeNode(item, key)) as T;
  }

  if (value && typeof value === "object") {
    const entries = Object.entries(value as Record<string, unknown>).map(([entryKey, entryValue]) => [
      entryKey,
      normalizeNode(entryValue, entryKey),
    ]);

    return Object.fromEntries(entries) as T;
  }

  return value;
}

export function normalizeSpanishContent<T>(value: T) {
  return normalizeNode(value);
}
